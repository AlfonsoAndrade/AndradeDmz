    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Nombre;
        String Contraseña;
        
        Nombre=jTextField1.getText();
        Contraseña=new String(jTextField2.getText());
        
        //Instancia de ArrayList Usuarios
        Usuarios = new ArrayList<String>();       
        //Agregamos  elementos al ArrayList
        Usuarios.add("JesusAlfonso");
        Usuarios.add("PedroHernandez");
        Usuarios.add("PatriciaSaldaña");
        Usuarios.add("JordanAleman");
        Usuarios.add("YessicaElizabeth");
       
        //Instancia de ArrayList Passwords 
        Passwords= new ArrayList<String>();
        //Agregamos  elemenotos al ArrayList
        Passwords.add("jesus123");
        Passwords.add("pedroH");
        Passwords.add("patysal2");
        Passwords.add("jordan190");
        Passwords.add("yessi456");
       
        //Usamos variables de tipo booleanas para poder comparar datos
        boolean Usu,Contra;
        Usu=Usuarios.contains(Nombre);
        Contra=Passwords.contains(Contraseña);
        
        if(Usu== true && Contra==true)
        {
            JOptionPane.showMessageDialog(null, "¡Login Correcto");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField1.requestFocus();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Login Incorrecto");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField1.requestFocus();
        }    
    }//GEN-LAST:event_jButton1ActionPerformed

